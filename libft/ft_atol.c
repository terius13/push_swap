#include "libft.h"

long     ft_atol(const char *nptr)
{
        long     neg;
        int     i;
        long     num;

        i = 0;
        neg = 1;
        num = 0;
        while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
                i++;
        if (nptr[i] == '-' || nptr[i] == '+')
        {
                if (nptr[i] == '-')
                        neg = neg * -1;
                i++;
        }
        while (nptr[i] >= '0' && nptr[i] <= '9')
        {
                num = num * 10 + (nptr[i] - '0');
                i++;
        }
        return (num * neg);
}
